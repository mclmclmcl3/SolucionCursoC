
#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << "Raiz Cuadrada 65:" << sqrt(65) << endl;
    cout << "Raiz Cuadrada 65 redondeada: " << round(sqrt(65)) << endl;
    cout << "Redondeo a la baja" << floor(2.6) << endl;
    cout << "Redondeo a la alta" << ceil(2.2) << endl;
    cout << "Logaritmo de 2: " << pow(3, 2) << endl;
    cout << "Elevar 3 al cuadrado: " << log(2) << endl;

    system("pause");
    return 0;
}

